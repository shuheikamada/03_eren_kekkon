$(document).ready(function() {
  var ua = navigator.userAgent;
        var clickoffset = 0;
  if (ua.indexOf('iPhone') > 0 || ua.indexOf('iPod') > 0 || ua.indexOf('Android') > 0 && ua.indexOf('Mobile') > 0 || ua.indexOf('iPad') > 0 || ua.indexOf('Android') > 0) {
      clickoffset = 50;
  } else {
      clickoffset = 100;
  }

    $('a').click(function(e) {

      /* ページ内遷移の場合のみブラウザ標準動作の抑制 */
      var prophref = $(this).prop("href"), //リンク先を絶対パスとして取得
      hrefPageUrl = prophref.split("#")[0], //リンク先を絶対パスについて、#より前のURLを取得
      currentUrl = location.href, //現在のページの絶対パスを取得
      currentUrl = currentUrl.split("#")[0]; //現在のページの絶対パスについて、#より前のURLを取得
      if(hrefPageUrl == currentUrl){
          e.preventDefault();
      }

        var speed = 500;

        var href= $(this).attr("href");

        var target = $(href == "#" || href == "" ? 'html' : href);

        var position = target.offset().top - clickoffset;

        $("html, body").animate({scrollTop:position}, speed, "swing");

    });

    $(window).scroll(function() {

      // スクロール量を変数に格納
      var sc = $(this).scrollTop();

      // メニューバースクロール固定
      if(sc>770){
          $('.menu').addClass('menuFix');
      }else{
          $('.menu').removeClass('menuFix');
      }


    });

	});
